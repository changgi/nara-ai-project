def main():
    print()
    print("=" * 50)
    print("  NARA-AI 데모 데이터 인덱싱")
    print("=" * 50)
    print()

    db_path = str(PROJECT_ROOT / "data" / "db" / "cpu_vectors.pkl")
    embedder = CPUEmbedder(db_path=db_path)

    count = embedder.index_documents(DEMO_RECORDS)
    print(f"  인덱싱 완료: {count}건")
    print(f"  저장 경로:   {db_path}")
    print()

    # 검색 테스트
    queries = [
        "한국전쟁 외교 문서",
        "기록물 관리 법률",
        "비밀해제 공개",
        "OCR 디지털화",
        "AI 기반 기록관리",
        "독립운동 해외 기록",
        "세종실록 한자",
    ]

    print("  === 검색 테스트 ===")
    print()
    for q in queries:
        results = embedder.search(q, top_k=3)
        print(f'  "{q}"')
        for i, r in enumerate(results):
            print(f"    [{i+1}] {r.title} (점수: {r.score:.4f})")
        print()

    print("=" * 50)
    print("  데이터 인덱싱 완료!")
    print("  웹 검색: python run.py --server → http://localhost:8080")
    print("=" * 50)


if __name__ == "__main__":
    main()
